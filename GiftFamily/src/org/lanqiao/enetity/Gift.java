package org.lanqiao.enetity;

public class Gift {
	
	//商品编号
	private int gift_id;
	//商品名称
	private String gift_name;
	//商品价格
	private int gift_price;
	//商品数量
	private int gift_num;
	//商品颜色
	private String gift_color;
	//商品规格
	private String gift_spect;
	//商品售卖数量
	private String gift_sell;
	//商品类型
	private String gift_type;
	//商品图片
	private String gift_img;

	public String getGift_img() {
		return gift_img;
	}
	public void setGift_img(String gift_img) {
		this.gift_img = gift_img;
	}
	public int getGift_id() {
		return gift_id;
	}
	public void setGift_id(int gift_id) {
		this.gift_id = gift_id;
	}
	public String getGift_name() {
		return gift_name;
	}
	public void setGift_name(String gift_name) {
		this.gift_name = gift_name;
	}
	public int getGift_price() {
		return gift_price;
	}
	public void setGift_price(int gift_price) {
		this.gift_price = gift_price;
	}
	public int getGift_num() {
		return gift_num;
	}
	public void setGift_num(int gift_num) {
		this.gift_num = gift_num;
	}
	public String getGift_color() {
		return gift_color;
	}
	public void setGift_color(String gift_color) {
		this.gift_color = gift_color;
	}
	public String getGift_spect() {
		return gift_spect;
	}
	public void setGift_spect(String gift_spect) {
		this.gift_spect = gift_spect;
	}
	public String getGift_sell() {
		return gift_sell;
	}
	public void setGift_sell(String gift_sell) {
		this.gift_sell = gift_sell;
	}
	public String getGift_type() {
		return gift_type;
	}
	public void setGift_type(String gift_type) {
		this.gift_type = gift_type;
	}
}
